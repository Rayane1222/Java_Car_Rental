    //populte jTable with brand 
    public void populateJtableWithBooking()
    {
        //clear array list 
        bok_list.clear();
        //populate arraylist
        bok_list = bok.BookingList();   
        //jtable columns
        String[] columnsName = {"ID","Car Id","Customer ID","PickUp City","PickUp Address","PickUp Date","PickUp Time","DropOff City","DropOff Address",
            "DropOff Date","DropOff Time","Total Price"};
        
        //jtable rows 
        Object[][] rows = new Object[bok_list.size()][columnsName.length];
        
        for(int i=0;i<bok_list.size();i++)
        {
            rows[i][0]  =bok_list.get(i).getId();
            rows[i][1]  =bok_list.get(i).getCar_id(); 
            rows[i][2]  =bok_list.get(i).getCustomer_id();
            rows[i][3]  =bok_list.get(i).getPickup_city();
            rows[i][4]  =bok_list.get(i).getPickup_address();
            rows[i][5]  =bok_list.get(i).getPickup_date();
            rows[i][6]  =bok_list.get(i).getPickup_time();
            rows[i][7]  =bok_list.get(i).getDropoff_city();
            rows[i][8]  =bok_list.get(i).getDropoff_address();
            rows[i][9]  =bok_list.get(i).getDropoff_date();
            rows[i][10]  =bok_list.get(i).getDropoff_time();
            rows[i][11]  =bok_list.get(i).getTotal_price();
            
        }
        
        DefaultTableModel model = new DefaultTableModel(rows,columnsName);
        jTable_Booking.setModel(model);
    }

