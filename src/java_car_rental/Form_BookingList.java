            String pickup_time = jTable_Booking.getValueAt(index, 6).toString();
            String dropoff_city = jTable_Booking.getValueAt(index, 7).toString();
            String dropoff_address = jTable_Booking.getValueAt(index, 8).toString();
            String dropoff_date = jTable_Booking.getValueAt(index, 9).toString();
            String dropoff_time = jTable_Booking.getValueAt(index, 10).toString();
            String total_price = jTable_Booking.getValueAt(index, 11).toString();
            String customer_fullname = new Customer().getCustomerById(Integer.valueOf(customer_id)).getFullname();
            String car_Model = new Car().getCarById(Integer.valueOf(customer_id)).getModel();
            
            
            
            Date dateNow = new Date();
            // Afficher la date et la localisation
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
            dateFormat.setTimeZone(TimeZone.getDefault());
            String dateStr = dateFormat.format(dateNow);
            
            
            
            
            
            bw.write("Badeel");
            bw.write("\n");
            bw.write("\n");
            bw.write("Date et heure de la facture : "+dateStr);
            bw.write("\n");
            bw.write("\n");
            bw.write("\n");
            bw.write("\n");
            bw.write("Booking ID : "+id);
            bw.write("\n---------------------------------\n");
            bw.write("Customer ID : "+customer_id+"  Customer FullName : "+customer_fullname);
            bw.write("\n---------------------------------\n");
            bw.write("Car ID : "+car_id+"  Car Model : "+car_Model);
            bw.write("\n---------------------------------\n");
            bw.write("Pick UP City  : "+pickup_city);
            bw.write("\n---------------------------------\n");
            bw.write("Pick UP Address: "+pickup_address);
            bw.write("\n---------------------------------\n");
            bw.write("Pick UP Date: "+pickup_date);
            bw.write("\n---------------------------------\n");
            bw.write("Pick UP Time: "+pickup_time);
            bw.write("\n---------------------------------\n");
            bw.write("Drop Off City: "+dropoff_city);
            bw.write("\n---------------------------------\n");
            bw.write("Drop Off Address: "+dropoff_address);
            bw.write("\n---------------------------------\n");
            bw.write("Drop Off Date: "+dropoff_date);
            bw.write("\n---------------------------------\n");
            bw.write("Drop Off Time: "+dropoff_time);
            bw.write("\n---------------------------------\n");
            bw.write("Total Price : "+total_price);
            
            
